package Models;

public class Point {
    private int x_coordinate;
    private int y_coordinate;

    public void setX(int x) {
        this.x_coordinate = x;
    }

    public void setY(int y) {
        this.y_coordinate = y;
    }

    public int getX() {
        return x_coordinate;
    }

    public int getY() {
        return y_coordinate;
    }

}
